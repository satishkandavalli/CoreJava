package com.java.literals;

public class SimpleAndCompoundOperators {
    public static void main(String[] args) {
        //Simple assignment operators
        int a=10;
        a=a+20;
        System.out.println(a);
        //Compound assignment operators
        int b1= -10;
        b1 +=10;
        System.out.println(b1);
        //Compound operators +=, -=, *=, /=
    }
}
