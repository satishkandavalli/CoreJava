package literals;

public class EscapeCharacters {
    public static void main(String[] args) {
        System.out.println("Satish\nKandavalli"); //new line
        System.out.println("Satish\tKandavalli"); //Add space
        System.out.println("Satish\rKandavalli"); //Carriage return
        System.out.println("Satish\" this"); //Escape sequence
        System.out.println("Satish\' this"); //Escape sequence
    }
}
