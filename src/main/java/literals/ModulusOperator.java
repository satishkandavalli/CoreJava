package literals;

public class ModulusOperator {
    public static void main(String[] args)
    {
        int a =37;
        int b=2;
        double modulus = a%b;
        double remainder = a/b;
        //Modulus % gives us remainder
        //and division operator gives us quotient
        System.out.println("THis modulus value is " + modulus);
        System.out.println("THis remainder value is " + remainder);
    }
}
